#include "libft.h"

char				*ft_strcat(char *s1, const char *s2)
{
	size_t				g;
	size_t				k;

	g = -1;
	k = -1;
	while (s1[++g] != '\0')
		;
	while (s2[++k] != '\0')
		s1[g++] = s2[k];
	s1[g] = '\0';
	return (s1);
}
